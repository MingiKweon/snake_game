void drawMap(Board board)
{

    for(int i = 0; i < 21; i++){
        for(int j = 0; j < 21; j++)
        {
            char a;
            switch (mapArray[i][j]) {
                case 0:
                    a = ' '; // 빈 공간
                    break;
                case 1:
                    a = '●'; // 벽
                    break;
                case 2:
                    a = '*';
                    break;
                case 3:
                    a = 'H';
                case 4:
                    a = 'B';

            }
            mvwprintw(board.getWindow(), i + 10, j + 10, "%c", a);

        }
    }
}