void drawMap()
{
    for(int i = 0; i < 21; i++){
        for(int j = 0; j < 21; j++)
        {
            char a;
            switch (map[i][j]) {
                case 0:
                    a = ' '; // 빈 공간
                    break;
                case 1:
                    a = '#'; // 벽
                    break;
                case 2:
                    a = '*'; // 특별한 픽셀
                    break;
                default:
                    a = '?'; // 예외 처리
                    break;
            }
            mvwprintw(win, i + 1, j + 1, "%c", a);
            wrefresh(win);
        }
    }
}

